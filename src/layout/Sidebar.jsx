// ── SessionLink: forwards session_id state on every navigation ────
function SessionLink({ to, className, children }) {
  const location = useLocation();
  return (
    <NavLink
      to={to}
      state={location.state}
      className={className}
    >
      {children}
    </NavLink>
  );
}

